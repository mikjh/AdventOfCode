using Printf

function process(comma_separated_list)
    list_to_process = map(x->parse(Int64,x),split(comma_separated_list,','))

    op_index = 1;

    output = Int[]
    while true
        op_with_args = list_to_process[op_index]

        #println(list_to_process)

        @printf("op_index: %d", op_index)
        println("command")
        println(op_with_args)

        if op_with_args == 99
            break;
        end

        op_digits = digits(op_with_args)

        op = zeros(Int,1,2)
        if length(op_digits) > 1
            op = op_digits[1:2]
        else
            op[1] = op_digits[1]
        end
        args = zeros(Int,3,1)
        if length(op_digits) > 2
            args[1:length(op_digits[3:end])] = op_digits[3:end]
        end
        print(op)
        print(args)

        if op[1] == 3
            ind1 = list_to_process[op_index+1]+1
            list_to_process[ind1] = args[1]
            op_index+=2
            continue
        end

        if op[1] == 4
            ind1 = list_to_process[op_index+1]+1
            el1 = 0
            if args[1] == 0
                el1 = list_to_process[ind1]
            elseif args[1] == 1
                el1 = list_to_process[op_index+1]
            end
            push!(output,el1)
            println("output")
            println(output)
            op_index+=2
            continue
        end

        ind1 = list_to_process[op_index+1]+1
        el1 = 0
        if args[1] == 0
            el1 = list_to_process[ind1]
        elseif args[1] == 1
            el1 = list_to_process[op_index+1]
        end

        ind2 = list_to_process[op_index+2]+1
        el2 = 0
        if args[2] == 0
            el2 = list_to_process[ind2]
        elseif args[2] == 1
            el2 = list_to_process[op_index+2]
        end

        @printf("(%d,%d)", el1,el2)

        if op[1] == 1
            ind3 = list_to_process[op_index+3]+1
            list_to_process[ind3] = el1 + el2
            op_index+=4
        elseif op[1] == 2
            ind3 = list_to_process[op_index+3]+1
            list_to_process[ind3] = el1 * el2
            op_index+=4
        elseif op[1] == 5
            if el1 != 0
                op_index = el2+1
            else
                op_index += 3
            end
        elseif op[1] == 6
            if el1 == 0
                op_index = el2+1
            else
                op_index += 3
            end
        elseif op[1] == 7
            ind3 = list_to_process[op_index+3]+1
            if el1 < el2
                list_to_process[ind3] = 1
            else
                list_to_process[ind3] = 0
            end
            op_index+=4
        elseif op[1] == 8
            ind3 = list_to_process[op_index+3]+1
            if el1 == el2
                list_to_process[ind3] = 1
            else
                list_to_process[ind3] = 0
            end
            op_index+=4
        end
    end

    return output
end

